export const inspirationCategories = categoryItems.map((category) => ({
  ...category,
  count: category.inspirations.length
}));

export function getInspirationCategoryBySlug(slug) {
  return inspirationCategories.find((category) => category.slug === slug);
}

export function getRelatedInspirations(category, inspiration, limit = 3) {
  if (!category || !inspiration) return [];

  return category.inspirations
    .filter((item) => item.id !== inspiration.id)
    .slice(0, limit);
}
